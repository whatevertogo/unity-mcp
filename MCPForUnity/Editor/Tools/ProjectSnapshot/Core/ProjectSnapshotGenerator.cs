        #region Priority Sorting

        private static int CalculatePrefabPriority(PrefabInfo prefab, SnapshotOptions options, Dictionary<string, int> referenceCounts)
        {
            int score = 0;

            int sceneCount = prefab.UsedInScenes?.Count ?? 0;
            score += Math.Min(100, sceneCount * 25);

            int refCount = referenceCounts.GetValueOrDefault(prefab.Path, 0);
            prefab.ReferenceCount = refCount;
            score += Math.Min(50, refCount * 5);

            var fileName = Path.GetFileNameWithoutExtension(prefab.Path).ToLower();
            var keywords = options.CoreNamingKeywords ?? Array.Empty<string>();
            int keywordMatches = 0;
            foreach (var keyword in keywords)
            {
                if (fileName.Contains(keyword.ToLower()))
                {
                    keywordMatches++;
                }
            }
            score += Math.Min(30, keywordMatches * 10);

            int pathDepth = prefab.Path.Count(c => c == '/' || c == '\\');
            int depthScore = Math.Max(0, 10 - pathDepth);
            score += depthScore;

            if (prefab.HasParentPrefab)
            {
                score -= 20;
            }

            return Math.Max(1, score);
        }

        private static List<PrefabInfo> PrioritizePrefabs(List<PrefabInfo> prefabs, SnapshotOptions options)
        {
            if (prefabs.Count == 0) return prefabs;

            var referenceCounts = new Dictionary<string, int>();
            foreach (var prefab in prefabs)
            {
                foreach (var dep in prefab.Dependencies)
                {
                    if (dep.Type == "Prefab")
                    {
                        referenceCounts.TryGetValue(dep.Path, out int current);
                        referenceCounts[dep.Path] = current + 1;
                    }
                }
            }

            foreach (var prefab in prefabs)
            {
                prefab.PriorityScore = CalculatePrefabPriority(prefab, options, referenceCounts);
            }

            var sorted = prefabs.OrderByDescending(p => p.PriorityScore).ToList();

            int limit = options.MaxPrefabsInSnapshot > 0 ? options.MaxPrefabsInSnapshot : sorted.Count;
            return sorted.Take(limit).ToList();
        }

        private static Dictionary<string, List<(string Type, string Path)>> CategorizeDependencies(List<(string Type, string Path)> dependencies)
        {
            var categories = new Dictionary<string, List<(string, string)>>();

            foreach (var dep in dependencies)
            {
                string category;
                switch (dep.Type)
                {
                    case "Script":
                        category = "Scripts";
                        break;
                    case "Prefab":
                        category = "Base";
                        break;
                    case "Material":
                    case "Texture":
                    case "Mesh":
                    case "Animation":
                    case "Audio":
                    case "Shader":
                        category = "Resources";
                        break;
                    default:
                        category = IsConfigType(dep.Type) ? "Config" : "Other";
                        break;
                }

                if (!categories.ContainsKey(category))
                    categories[category] = new List<(string, string)>();

                categories[category].Add(dep);
            }

            return categories;
        }

        private static bool IsConfigType(string type)
        {
            return type == "Asset" || type == "GameData" || type == "Settings" ||
                   type == "Data" || type == "Config";
        }

        private static string FormatDependencyTree(List<(string Type, string Path)> dependencies, SnapshotOptions options)
        {
            var sb = new StringBuilder();
            int topN = options.TopDependenciesToShow > 0 ? options.TopDependenciesToShow : 3;

            var categories = CategorizeDependencies(dependencies);
            var categoryOrder = new[] { "Scripts", "Base", "Config", "Resources", "Other" };

            foreach (var category in categoryOrder)
            {
                if (!categories.ContainsKey(category) || categories[category].Count == 0)
                    continue;

                var items = categories[category];
                var icon = category switch
                {
                    "Scripts" => "📄",
                    "Base" => "🧩",
                    "Config" => "⚙️",
                    "Resources" => "🎨",
                    _ => "📦"
                };

                sb.AppendLine($"  {icon} **{category}** [{items.Count}]");

                foreach (var item in items.Take(topN))
                {
                    var name = Path.GetFileNameWithoutExtension(item.Path);
                    sb.AppendLine($"    - `{name}` ({item.Type})");
                }

                if (items.Count > topN)
                {
                    sb.AppendLine($"    - ... ({items.Count - topN} more)");
                }
            }

            return sb.ToString();
        }

