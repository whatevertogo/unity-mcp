        #region Content Generation Methods (moved from ProjectSnapshotTool)

        private static string GenerateSnapshotContent(SnapshotOptions options)
        {
            var sb = new StringBuilder();

            GenerateEnhancedHeader(sb, options);
            GenerateEnhancedCoreArchitecture(sb, options);

            if (options.IncludeDataSchemas)
            {
                GenerateCodeStructureSection(sb, options);
            }

            DirectoryAnalyzer.GenerateDirectoryMap(sb, options);

            if (options.IncludeDependencies)
            {
                AssetRegistryAnalyzer.GenerateAssetRegistry(sb, options);
            }

            if (options.IncludeDataSchemas)
            {
                GenerateLightweightDataSchema(sb, options);
            }

            if (options.SeparateDependenciesFile)
            {
                GenerateDependenciesNote(sb, options);
            }

            GenerateQuickReference(sb);
            GenerateAIGuide(sb);

            return sb.ToString();
        }

        private static void GenerateEnhancedHeader(StringBuilder sb, SnapshotOptions options)
        {
            sb.AppendLine("# Project Snapshot");
            sb.AppendLine($"*Generated on {DateTime.UtcNow:yyyy-MM-dd HH:mm:ss} UTC*");
            sb.AppendLine();

            var stats = ArchitectureAnalyzer.GetProjectStatistics();
            sb.AppendLine($"*Quick Stats: {stats.GetValueOrDefault("total_scripts", 0)} scripts, " +
                         $"{stats.GetValueOrDefault("scenes", 0)} scenes, " +
                         $"{stats.GetValueOrDefault("scriptable_objects", 0)} ScriptableObjects*");
            sb.AppendLine();
        }

        private static void GenerateEnhancedCoreArchitecture(StringBuilder sb, SnapshotOptions options)
        {
            sb.AppendLine("## [SECTION 1: Core Architecture]");
            sb.AppendLine();

            var archType = ArchitectureAnalyzer.DetectArchitectureType();
            sb.AppendLine($"**Architecture Type**: `{archType.Type}`");
            if (!string.IsNullOrEmpty(archType.Confidence))
            {
                sb.AppendLine($"**Detection**: {archType.Confidence}");
            }
            sb.AppendLine();

            var patterns = ArchitectureAnalyzer.DetectArchitecturalPatterns();
            if (patterns.Count > 0)
            {
                sb.AppendLine("**Detected Patterns**:");
                foreach (var pattern in patterns.OrderByDescending(p => p.Value))
                {
                    sb.AppendLine($"- `{pattern.Key}`: {pattern.Value} occurrences");
                }
                sb.AppendLine();
            }

            sb.AppendLine("**Entry Points**:");
            var entryPoints = ArchitectureAnalyzer.FindEntryPoints(options);
            if (entryPoints.Count > 0)
            {
                foreach (var entry in entryPoints.Take(10))
                {
                    var fileName = Path.GetFileNameWithoutExtension(entry.Path);
                    sb.AppendLine($"- `{fileName}` - {entry.Reason}");
                    sb.AppendLine($"  Path: `{entry.Path}`");
                }
                if (entryPoints.Count > 10)
                {
                    sb.AppendLine($"  ... and {entryPoints.Count - 10} more entry points");
                }
            }
            else
            {
                sb.AppendLine("- _No obvious entry point detected_");
            }
            sb.AppendLine();

            sb.AppendLine("**Asset Loading Strategy**:");
            var loadingStrategy = ArchitectureAnalyzer.DetectLoadingStrategy();
            sb.AppendLine($"- **Method**: `{loadingStrategy.Method}`");
            if (loadingStrategy.Indicators.Count > 0)
            {
                sb.AppendLine($"- **Details**:");
                foreach (var indicator in loadingStrategy.Indicators.Take(5))
                {
                    sb.AppendLine($"  - {indicator}");
                }
            }
            sb.AppendLine();

            sb.AppendLine("**Key Manager / Service Classes**:");
            var managers = ArchitectureAnalyzer.FindManagerClasses(options);
            if (managers.Count > 0)
            {
                foreach (var manager in managers.Take(15))
                {
                    var fileName = Path.GetFileNameWithoutExtension(manager);
                    var ns = Path.GetDirectoryName(manager)?.Replace("Assets/", "").Replace('/', '.');
                    sb.AppendLine($"- `{fileName}` ({ns})");
                }
                if (managers.Count > 15)
                {
                    sb.AppendLine($"  ... and {managers.Count - 15} more managers");
                }
            }
            else
            {
                sb.AppendLine("- _No manager classes detected_");
            }
            sb.AppendLine();
        }

        private static void GenerateCodeStructureSection(StringBuilder sb, SnapshotOptions options)
        {
            sb.AppendLine("## [SECTION 2: Code Structure]");
            sb.AppendLine();

            var codeStructure = CodeStructureAnalyzer.AnalyzeCodeStructure(options);

            var namespaces = codeStructure.GetValueOrDefault("namespaces") as IEnumerable<object>;
            if (namespaces != null)
            {
                sb.AppendLine("**Namespaces**:");
                foreach (var ns in namespaces.Cast<dynamic>().Take(10))
                {
                    var name = ns.GetType().GetProperty("name")?.GetValue(ns);
                    var count = ns.GetType().GetProperty("count")?.GetValue(ns);
                    var keyClasses = ns.GetType().GetProperty("key_classes")?.GetValue(ns) as IEnumerable<object>;

                    sb.AppendLine($"- `{name}` ({count} classes)");
                    if (keyClasses != null)
                    {
                        var classes = string.Join(", ", keyClasses.Cast<object>().Take(3));
                        if (!string.IsNullOrEmpty(classes))
                        {
                            sb.AppendLine($"  Key: {classes}...");
                        }
                    }
                }
                sb.AppendLine();
            }

            var categories = codeStructure.GetValueOrDefault("components_by_category") as IDictionary<string, object>;
            if (categories != null)
            {
                sb.AppendLine("**By Category**:");
                foreach (var kvp in categories.OrderByDescending(k => ((IEnumerable<object>)k.Value).Count()))
                {
                    var items = (IEnumerable<object>)kvp.Value;
                    sb.AppendLine($"- `{kvp.Key}`: {items.Count()} types");
                }
                sb.AppendLine();
            }
        }

        private static string GenerateDependenciesFile(SnapshotOptions options)
        {
            var sb = new StringBuilder();
            int tokenEstimate = 0;
            int maxTokens = options.MaxDependencyTokens > 0 ? options.MaxDependencyTokens : 8000;

            sb.AppendLine("# Asset Dependencies");
            sb.AppendLine($"*Generated on {DateTime.UtcNow:yyyy-MM-dd HH:mm:ss} UTC*");
            sb.AppendLine($"*Generated for Project_Snapshot.md*");
            sb.AppendLine();
            tokenEstimate += 30;

            var stats = ArchitectureAnalyzer.GetProjectStatistics();
            sb.AppendLine($"## Project Overview");
            sb.AppendLine();
            tokenEstimate += 20;

            sb.AppendLine($"| Metric | Count |");
            sb.AppendLine($"|--------|-------|");
            sb.AppendLine($"| Scripts | {stats.GetValueOrDefault("total_scripts", 0)} |");
            sb.AppendLine($"| Scenes | {stats.GetValueOrDefault("scenes", 0)} |");
            sb.AppendLine($"| ScriptableObjects | {stats.GetValueOrDefault("scriptable_objects", 0)} |");
            sb.AppendLine($"| JSON Data Files | {stats.GetValueOrDefault("json_files", 0)} |");
            sb.AppendLine();
            tokenEstimate += 20;

            if (tokenEstimate > maxTokens)
            {
                sb.AppendLine("> **[CIRCUIT BREAKER]**: Token budget exceeded.");
                return sb.ToString();
            }

            // Scenes section
            var scenes = AssetRegistryAnalyzer.FindCoreScenes(options);
            if (scenes.Count > 0)
            {
                sb.AppendLine($"## Scenes ({scenes.Count})");
                sb.AppendLine();
                tokenEstimate += 15;

                foreach (var scene in scenes)
                {
                    if (tokenEstimate > maxTokens) break;

                    var buildInfo = scene.IsBuildSettingsIncluded ? " ✅ In Build" : "";
                    sb.AppendLine($"### {scene.Name}{buildInfo}");
                    sb.AppendLine();
                    tokenEstimate += 10;

                    sb.AppendLine($"- **Path**: `{scene.Path}`");
                    sb.AppendLine($"- **Estimated Objects**: {scene.GameObjectCount}");
                    sb.AppendLine($"- **Dependencies**: {scene.Dependencies.Count}");
                    tokenEstimate += 15;

                    if (scene.Dependencies.Count > 0 && scene.Dependencies.Count <= 15)
                    {
                        sb.AppendLine();
                        sb.AppendLine("**Direct Dependencies**:");
                        foreach (var dep in scene.Dependencies)
                        {
                            sb.AppendLine($"- `{Path.GetFileName(dep.Path)}` ({dep.Type})");
                        }
                        tokenEstimate += 10 + scene.Dependencies.Count * 5;
                    }
                    sb.AppendLine();
                }
            }

            if (tokenEstimate > maxTokens)
            {
                sb.AppendLine("> **[CIRCUIT BREAKER]**: Token budget exceeded.");
                return sb.ToString();
            }

            // Prefabs section
            var prefabs = AssetRegistryAnalyzer.FindCorePrefabs(options);
            if (prefabs.Count > 0)
            {
                var sortedPrefabs = PrioritizePrefabs(prefabs, options);

                sb.AppendLine($"## Prefabs ({sortedPrefabs.Count} prioritized)");
                sb.AppendLine();
                sb.AppendLine("> Prefabs are sorted by priority: scene usage > dependencies > naming > path depth");
                sb.AppendLine();
                tokenEstimate += 30;

                for (int i = 0; i < sortedPrefabs.Count; i++)
                {
                    if (tokenEstimate > maxTokens) break;

                    var prefab = sortedPrefabs[i];
                    var fileName = Path.GetFileNameWithoutExtension(prefab.Path);
                    var guid = AssetDatabase.AssetPathToGUID(prefab.Path);

                    string priorityBadge = prefab.PriorityScore >= 100 ? " 🔥 HIGH" :
                                          prefab.PriorityScore >= 50 ? " ⭐ MEDIUM" : " 📄 LOW";

                    sb.AppendLine($"### {fileName}{priorityBadge}");
                    sb.AppendLine();
                    tokenEstimate += 10;

                    sb.AppendLine($"- **Priority Score**: {prefab.PriorityScore}");
                    sb.AppendLine($"- **Path**: `{prefab.Path}`");
                    sb.AppendLine($"- **GUID**: `{guid}`");
                    sb.AppendLine($"- **Dependencies**: {prefab.Dependencies.Count}");

                    if (prefab.UsedInScenes != null && prefab.UsedInScenes.Count > 0)
                    {
                        sb.AppendLine($"- **Used In**: {prefab.UsedInScenes.Count} scene(s)");
                        foreach (var scene in prefab.UsedInScenes.Take(3))
                        {
                            sb.AppendLine($"  - `{Path.GetFileNameWithoutExtension(scene)}`");
                        }
                        if (prefab.UsedInScenes.Count > 3)
                        {
                            sb.AppendLine($"  - ... and {prefab.UsedInScenes.Count - 3} more");
                        }
                    }

                    if (prefab.ReferenceCount.HasValue && prefab.ReferenceCount.Value > 0)
                    {
                        sb.AppendLine($"- **Referenced By**: {prefab.ReferenceCount.Value} other asset(s)");
                    }

                    if (prefab.HasParentPrefab && !string.IsNullOrEmpty(prefab.ParentPrefabPath))
                    {
                        sb.AppendLine($"- **Parent Prefab**: `{Path.GetFileNameWithoutExtension(prefab.ParentPrefabPath)}`");
                    }

                    tokenEstimate += 40;

                    if (prefab.Dependencies.Count > 0)
                    {
                        sb.AppendLine();
                        if (prefab.Dependencies.Count <= 5)
                        {
                            sb.AppendLine("**Dependencies** (tree view):");
                            var tree = FormatDependencyTree(prefab.Dependencies, options);
                            sb.Append(tree);
                            tokenEstimate += 10 + prefab.Dependencies.Count * 20;
                        }
                        else
                        {
                            sb.AppendLine($"**Dependencies**: {prefab.Dependencies.Count} assets");
                            sb.AppendLine();
                            sb.AppendLine("*Use `inspect_dependency` with focus_path for detailed analysis.*");
                            tokenEstimate += 15;
                        }
                    }
                    sb.AppendLine();
                }

                if (tokenEstimate > maxTokens)
                {
                    sb.AppendLine();
                    sb.AppendLine("> **[CIRCUIT BREAKER]**: Token budget exceeded. Some prefabs were truncated.");
                }
            }

            return sb.ToString();
        }

